<script>
  function getRandomInt(min, max) {
      return Math.floor(Math.random() * (max - min + 1)) + min;
  }

  function updateChartData(chart, min, max, count) {
      let newData = Array.from({ length: count }, () => getRandomInt(min, max));
      chart.data.datasets[0].data = newData;
      chart.update();
  }

  function updateDashboardData() {
      // Update card values dynamically
      document.querySelectorAll(".card h4").forEach((element, index) => {
          if (index === 0) {
              element.innerText = `$${getRandomInt(20, 100)}k`; // Today's Money
          } else if (index === 1) {
              element.innerText = getRandomInt(1000, 2500); // Today's Users
          } else if (index === 2) {
              element.innerText = `$${getRandomInt(50000, 200000)}`; // Sales
          }
      });

      document.querySelectorAll(".card-footer p span").forEach((element) => {
          element.innerText = `+${getRandomInt(1, 10)}%`; // Growth percentage
      });

      // Update charts with new random data
      updateChartData(chartBars, 20, 100, 7); // Property Views
      updateChartData(chartLine, 100, 500, 12); // Daily Sales
  }

  // Initialize Bar Chart (Property Views)
  var ctxBars = document.getElementById("chart-bars").getContext("2d");
  var chartBars = new Chart(ctxBars, {
      type: "bar",
      data: {
          labels: ["M", "T", "W", "T", "F", "S", "S"],
          datasets: [
              {
                  label: "Views",
                  backgroundColor: "#43A047",
                  data: [50, 45, 22, 28, 50, 60, 76],
                  borderRadius: 4,
              },
          ],
      },
      options: {
          responsive: true,
          maintainAspectRatio: false,
          plugins: { legend: { display: false } },
          scales: { y: { beginAtZero: true }, x: {} },
      },
  });

  // Initialize Line Chart (Daily Sales)
  var ctxLine = document.getElementById("chart-line").getContext("2d");
  var chartLine = new Chart(ctxLine, {
      type: "line",
      data: {
          labels: ["J", "F", "M", "A", "M", "J", "J", "A", "S", "O", "N", "D"],
          datasets: [
              {
                  label: "Sales",
                  borderColor: "#43A047",
                  backgroundColor: "transparent",
                  data: [120, 230, 130, 440, 250, 360, 270, 180, 90, 300, 310, 220],
                  borderWidth: 2,
                  pointRadius: 3,
              },
          ],
      },
      options: {
          responsive: true,
          maintainAspectRatio: false,
          plugins: { legend: { display: false } },
          scales: { y: { beginAtZero: true }, x: {} },
      },
  });

  // Auto-refresh every 15 seconds
  setInterval(updateDashboardData, 15000);
</script>

